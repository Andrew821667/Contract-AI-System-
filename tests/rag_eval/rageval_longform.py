def titles_for(q):
    ctx = get_legal_context(q, collections=["laws", "case_law"], n_results=3, max_chars=2000)
    return re.findall(r'\[(?:Законы и НПА|Судебная практика)\] — ([^\n]+)', ctx)


def hit(q, exp):
    return any(re.search(exp, t, re.I) for t in titles_for(q))


def main():
    n = len(PAIRS)
    short_ok = long_rw_ok = long_raw_ok = 0
    long_regr = []   # short✓ long✗ (rewrite) — целевой класс ошибок
    rescued = []     # long raw✗ → rewrite✓
    for topic, sq, lq, exp in PAIRS:
        settings.rag_rewrite = True
        s = hit(sq, exp)
        lr = hit(lq, exp)
        settings.rag_rewrite = False
        lraw = hit(lq, exp)
        short_ok += s; long_rw_ok += lr; long_raw_ok += lraw
        if s and not lr: long_regr.append(topic)
        if lr and not lraw: rescued.append(topic)
        flag = "  " if s == lr else ("↓↓" if s and not lr else "↑↑")
        print(f"{flag} short={'✓' if s else '✗'}  long_rw={'✓' if lr else '✗'}  "
              f"long_raw={'✓' if lraw else '✗'}   [{topic}]", flush=True)

    print("\n" + "=" * 72)
    print(f"LONG-FORM (парный short vs длинная история), тем: {n}, top-3")
    print("=" * 72)
    print(f"  short  (rewrite):  {short_ok}/{n} = {100*short_ok/n:.0f}%")
    print(f"  long   (rewrite):  {long_rw_ok}/{n} = {100*long_rw_ok/n:.0f}%   ← прод-путь на длинных")
    print(f"  long   (raw):      {long_raw_ok}/{n} = {100*long_raw_ok/n:.0f}%   ← без rewrite")
    print(f"  штраф за длину (short − long, rewrite): {100*(short_ok-long_rw_ok)/n:+.0f} пунктов")
    print(f"  спасено rewrite'ом на длинных (raw✗→rw✓): {len(rescued)}  {rescued}")
    if long_regr:
        print(f"  ⚠️ ДЛИННЫЕ РЕГРЕССИИ (short✓ long✗): {long_regr}")
    else:
        print(f"  ✅ длинных регрессий нет — длина не роняет находимость")


if __name__ == "__main__":
    main()
