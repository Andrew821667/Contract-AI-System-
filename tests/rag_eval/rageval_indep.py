if __name__ == "__main__":
    # запуск требует DSKEY (авто-рерайт). Импорт Q — без побочных эффектов.
    _key = os.environ.get("DSKEY", "").strip()
    if not _key:
        print("НЕТ DSKEY"); sys.exit(1)
    from config.settings import settings
    settings.deepseek_api_key = _key
    from src.services.admin_rag_retriever import get_legal_context

    def titles_for(q):
        ctx = get_legal_context(q, collections=["laws", "case_law"], n_results=3)
        return re.findall(r'\[(?:Законы и НПА|Судебная практика)\] — ([^\n]+)', ctx)

    raw_ok = rw_ok = 0
    gained = []; lost = []
    for topic, q, exp in Q:
        settings.rag_rewrite = False
        t_raw = titles_for(q)
        settings.rag_rewrite = True
        t_rw = titles_for(q)
        r_raw = any(re.search(exp, t, re.I) for t in t_raw)
        r_rw = any(re.search(exp, t, re.I) for t in t_rw)
        raw_ok += r_raw; rw_ok += r_rw
        if r_rw and not r_raw: gained.append(topic)
        if r_raw and not r_rw: lost.append((topic, t_rw))
        flag = "  " if r_raw == r_rw else ("↑↑" if r_rw else "↓↓")
        print(f"{flag} raw={'✓' if r_raw else '✗'} rw={'✓' if r_rw else '✗'}  [{topic}]", flush=True)

    N = len(Q)
    print(f"\n{'='*64}", flush=True)
    print(f"НЕЗАВИСИМЫЙ набор ({N} разговорных, held-out), top-3:", flush=True)
