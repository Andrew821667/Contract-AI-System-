ART = re.compile(r"(?:стать[еийяю]\w*|ст\.)\s*(\d+(?:\.\d+)?)", re.I)
def invented(ans, ctx):
    return sorted({a for a in ART.findall(ans) if a not in set(ART.findall(ctx))})

def triad(q, points):
    ctx = get_legal_context(q, collections=["laws", "case_law", "knowledge"],
                            n_results=3, max_chars=2000)
    sp = SYS_ANS + (f"\n\n# Правовая база и база знаний\n{ctx}" if ctx else "")
    a = gw.call(prompt=f"Пользователь: {q}", system_prompt=sp,
                response_format="text", temperature=0.0, max_tokens=700)
    a = (a if isinstance(a, str) else str(a)).strip()
    jp = (f"ВОПРОС: {q}\n\nЭТАЛОННЫЕ ПУНКТЫ:\n"
          + "\n".join(f"{i+1}. {p}" for i, p in enumerate(points))
          + f"\n\nКОНТЕКСТ:\n{ctx[:3500]}\n\nОТВЕТ:\n{a}\n\nJSON:")
    try:
        jo = gw.call(prompt=jp, system_prompt=SYS_JUDGE, response_format="text",
                     temperature=0.0, max_tokens=200)
        m = re.search(r"\{.*\}", jo if isinstance(jo, str) else str(jo), re.DOTALL)
        v = json.loads(m.group(0))
        cov = sum(1 for x in v.get("covered", []) if x) / max(1, len(points))
        gr = float(v.get("groundedness", 0)); fa = float(v.get("factuality", 0))
    except Exception as e:
        print(f"  судья упал на [{q[:30]}]: {e}"); cov = gr = fa = 0.0
    score = 0.25 * cov + 0.35 * gr + 0.40 * fa
    return score, cov, gr, fa, len(invented(a, ctx))

def main():
    ts = {"short": [0.0, 0.0, 0.0, 0.0, 0], "long": [0.0, 0.0, 0.0, 0.0, 0]}
    print(f"{'тема':22} | short score | long score |  Δ")
    print("-" * 60)
    for topic, sq, lq, pts in CASES:
        s = triad(sq, pts)
        l = triad(lq, pts)
        for i in range(5):
            ts["short"][i] += s[i]; ts["long"][i] += l[i]
        d = l[0] - s[0]
        flag = "  " if abs(d) < 0.08 else ("↓↓" if d < 0 else "↑↑")
        print(f"{flag}{topic:20} |   {s[0]:.2f}      |   {l[0]:.2f}     | {d:+.2f}", flush=True)
    n = len(CASES)
    print("\n" + "=" * 60)
    print(f"LONG-FORM ANSWER-EVAL, тем: {n} (short vs длинная история)")
    for arm in ("short", "long"):
        t = ts[arm]
        print(f"  {arm:5}: score={t[0]/n:.3f} | cov={t[1]/n:.2f} gr={t[2]/n:.2f} "
              f"fa={t[3]/n:.2f} | выдуманных={t[4]}")
    print(f"  Δ качества ответа (long − short): {(ts['long'][0]-ts['short'][0])/n:+.3f}")

if __name__ == "__main__":
    main()
